// Helper functions for filtering
export const getEventsByType = (events: SecurityEvent[], type: string): SecurityEvent[] => {
  if (type === 'all') return events;
  return events.filter(e => e.eventType === type);
};

export const getEventsByCamera = (events: SecurityEvent[], cameraId: string): SecurityEvent[] => {
  if (cameraId === 'all') return events;
  return events.filter(e => e.cameraId === cameraId);
};

export const getEventsByDateRange = (events: SecurityEvent[], startDate: Date, endDate: Date): SecurityEvent[] => {
  return events.filter(e => e.timestamp >= startDate && e.timestamp <= endDate);
};

export const getEventsByDetection = (events: SecurityEvent[], detection: 'persons' | 'vehicles' | 'packages' | 'animals'): SecurityEvent[] => {
  return events.filter(e => e.detections[detection] > 0);
};

export const searchEvents = (events: SecurityEvent[], query: string): SecurityEvent[] => {
  const lowerQuery = query.toLowerCase();
  return events.filter(e =>
    e.cameraName.toLowerCase().includes(lowerQuery) ||
    e.eventType.toLowerCase().includes(lowerQuery) ||
    e.cameraLocation.toLowerCase().includes(lowerQuery) ||
    e.zone?.toLowerCase().includes(lowerQuery) ||
    e.colors?.some(c => c.toLowerCase().includes(lowerQuery))
  );
};
